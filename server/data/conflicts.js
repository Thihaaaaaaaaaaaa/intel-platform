export function generateAlert() {
  const active = CONFLICTS.filter(c => c.status === 'active');
  const c = active[Math.floor(Math.random()*active.length)];
  return {
    id: `ALT${alertIdCounter++}`,
    conflictId: c.id,
    conflictName: c.name,
    type: ALERT_TYPES[Math.floor(Math.random()*ALERT_TYPES.length)],
    severity: ['low','medium','high','critical'][Math.floor(Math.random()*4)],
    description: ALERT_DESC[Math.floor(Math.random()*ALERT_DESC.length)],
    location: c.fronts[Math.floor(Math.random()*c.fronts.length)],
    source: ['Reuters','AP','ISW','ACLED','Local Sources','OSINT'][Math.floor(Math.random()*6)],
    timestamp: new Date().toISOString(),
    verified: Math.random() > 0.3,
  };
}
